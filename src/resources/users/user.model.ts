const uuid = require('uuid');

class User {
  constructor({ id = uuid.v4(), name, login, password } = {}) {
    this.id = id;
    this.name = name;
    this.login = login;
    this.password = password;
  }

  static toResponse(user) {
    const { id, name, login } = user;
    return { id, name, login };
  }
}

module.exports = { User };
